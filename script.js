const icon = document.getElementById("icon");
icon.onclick = function() {
  var fontNum = document.getElementById("fontNum");
  var iconNum = document.getElementById("iconNum");
  if (iconNum.style.display !== 'none') {
    iconNum.style.display = 'none';
    fontNum.style.display = 'block';
  }
  else {
    iconNum.style.display = 'block';
    fontNum.style.display = 'none';
  }
}
